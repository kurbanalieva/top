# 5. Двузначные числа с четной суммой цифр
print("Двузначные числа с четной суммой цифр:")
for num in range(10, 100):
    if (num // 10 + num % 10) % 2 == 0:
        print(num, end=' ')
print("\n")

# 7. Последовательность от 0 до 20 с отрицательными
print("Последовательность с отрицательными числами:")
for i in range(21):
    print(i, -i, end=' ')
print("\n")

