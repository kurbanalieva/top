# Вариант 1: остановка по вводу нуля
count = 0

while True:
    num = input("Введите число (0 для завершения): ")
    
    # Проверяем, если введено 0 - завершаем цикл
    if num == '0':
        break
    
    # Увеличиваем счетчик
    count += 1

print(f"Количество введенных чисел: {count}")

# Вариант 2: остановка по нажатию Enter
count = 0

while True:
    num = input("Введите число (Enter для завершения): ")
    
    # Проверяем, если введена пустая строка - завершаем цикл
    if num == '':
        break
    
    # Увеличиваем счетчик
    count += 1

print(f"Количество введенных чисел: {count}")





