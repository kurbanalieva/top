mark = 85  # создаем переменную mark и присваиваем ей значение 85

if mark >= 90:  # проверяем первое условие: если оценка больше или равна 90
    print("Отлично!")  # если условие истинно, выводим "Отлично!"
elif mark >= 70:  # если предыдущее условие ложно, проверяем следующее: больше или равно 70
    print("Хорошо")  # если это условие истинно, выводим "Хорошо"
elif mark >= 50:  # если предыдущие условия ложны, проверяем: больше или равно 50
    print("Удовлетворительно")  # если это условие истинно, выводим "Удовлетворительно"
else:  # если все предыдущие условия ложны
    print("Нужно постараться")  # выводим "Нужно постараться"
    
